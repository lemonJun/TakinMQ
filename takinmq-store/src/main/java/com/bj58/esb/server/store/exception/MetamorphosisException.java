package com.bj58.esb.server.store.exception;

public class MetamorphosisException extends Exception {
	static final long serialVersionUID = 35987565675L;
    public MetamorphosisException() {
        super();

    }


    public MetamorphosisException(final String message, final Throwable cause) {
        super(message, cause);

    }


    public MetamorphosisException(final String message) {
        super(message);

    }


    public MetamorphosisException(final Throwable cause) {
        super(cause);

    }

}